<h2>Move-in day: bring this, do this</h2>

<h3>Bring</h3>
<div class="check-item"><span class="checkbox"></span> <strong>Photo ID + insurance cards</strong> (Medicare, Medicaid, supplemental, LTC insurance)</div>
<div class="check-item"><span class="checkbox"></span> <strong>Complete medication list</strong> with doses + times, plus actual pill bottles (the facility's pharmacy will need to reconcile)</div>
<div class="check-item"><span class="checkbox"></span> <strong>List of all providers</strong> — PCP, specialists, dentist, podiatrist, dermatologist — with phone numbers</div>
<div class="check-item"><span class="checkbox"></span> <strong>Durable medical equipment</strong> — CPAP, walker, hearing aids (with batteries), glasses (with case and a backup), dentures (with case + adhesive)</div>
<div class="check-item"><span class="checkbox"></span> <strong>3-5 sets of comfortable clothes</strong>, non-slip shoes (a fall-prevention essential), pajamas, robe, slippers, undergarments</div>
<div class="check-item"><span class="checkbox"></span> <strong>Personal touches</strong> — 4-6 framed photos, a familiar blanket, a few books, a clock with large numbers, religious items if applicable</div>
<div class="check-item"><span class="checkbox"></span> <strong>Labeled belongings</strong> — laundry will go through the facility system; un-labeled items disappear</div>
<div class="check-item"><span class="checkbox"></span> <strong>POA + healthcare proxy documents</strong> — physical copies for the facility's chart</div>

<h3>Do</h3>
<div class="check-item"><span class="checkbox"></span> <strong>Arrive when admissions told you to arrive.</strong> Earlier = you're standing around. Later = the staff has moved on to other tasks.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Walk through the room with the admit nurse.</strong> Test the call light. Confirm bed-height + alarm setting if needed. Locate the water pitcher and put it within reach. Check the bathroom is accessible.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Get every name + role.</strong> Charge nurse, day-shift CNA, social worker, dietary aide. Write them down or photograph the badges.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Confirm the medication reconciliation.</strong> Sit with the nurse and walk through every med — name, dose, time. This single step prevents the majority of preventable first-week problems.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Stay through one meal.</strong> Eat with your loved one. See how the dining room actually runs. Meet a few residents at the table.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Establish your visit cadence.</strong> "I'll be back tomorrow at 2pm" beats "I'll come back soon." Predictability anchors them.</div>

<h2>Days 1-7: the disorientation phase</h2>
<p>
    The first week is hardest. Your loved one will likely:
</p>
<div class="check-item"><span class="checkbox"></span> Want to "go home" — sometimes constantly. Hold the line; this is normal.</div>
<div class="check-item"><span class="checkbox"></span> Sleep more than usual (adjustment fatigue) or less (anxiety + new environment)</div>
<div class="check-item"><span class="checkbox"></span> Eat less initially (new food, new pace, new tablemates)</div>
<div class="check-item"><span class="checkbox"></span> Be more confused even if they have no dementia (transition delirium is real and usually clears in 1-2 weeks)</div>
<div class="check-item"><span class="checkbox"></span> Have one bad day, then a better one, then another bad one — non-linear</div>

<p>
    <strong>What you should do this week:</strong> visit daily if
    possible. Short visits (30-60 min) are usually better than long
    ones. Eat together once. Don't take them out — staying in the
    facility builds the new mental map.
</p>

<h3>What's NOT normal in week 1</h3>
<div class="check-item"><span class="checkbox"></span> Significant weight loss (more than 3-4 pounds)</div>
<div class="check-item"><span class="checkbox"></span> A fall (any fall)</div>
<div class="check-item"><span class="checkbox"></span> A skin tear, bruise, or unexplained injury</div>
<div class="check-item"><span class="checkbox"></span> Medication missed for more than one dose</div>
<div class="check-item"><span class="checkbox"></span> Staff who can't tell you what your loved one ate yesterday</div>
<div class="check-item"><span class="checkbox"></span> A roommate situation that's clearly hostile</div>
<div class="check-item"><span class="checkbox"></span> Your loved one expressing fear of a specific staff member (always investigate)</div>

<h2>Days 7-14: the adjustment phase</h2>
<p>
    By the second week, you should see signs of routine forming:
    they recognize the morning CNA, they have a favorite seat in the
    dining room, they've found one activity they don't hate, they
    sleep through the night. If none of these have happened by day
    14, schedule a care-plan meeting (see below).
</p>

<h3>What you should do this week</h3>
<div class="check-item"><span class="checkbox"></span> Visit every other day, not daily. Let some routine form without you.</div>
<div class="check-item"><span class="checkbox"></span> Attend or call in to the 14-day care plan meeting if SNF, or the initial service plan meeting if AL.</div>
<div class="check-item"><span class="checkbox"></span> Meet the resident in the room next door + introduce yourself to the night-shift CNA.</div>
<div class="check-item"><span class="checkbox"></span> Take a photo together — for both of you, but mostly because the staff sees the loved one as part of a family.</div>

<h2>Days 14-30: the new normal</h2>
<p>
    By day 21, signs of settling: less "I want to go home", more
    routine, more interaction with other residents, more eating, less
    sleeping during the day. Some adjustment hiccups still — that's
    normal.
</p>

<h3>The 30-day care-plan meeting</h3>
<p>
    SNFs are required to hold one. ALs typically do too. Attend.
    Bring questions:
</p>
<div class="check-item"><span class="checkbox"></span> What's their weight trend?</div>
<div class="check-item"><span class="checkbox"></span> Any falls? Any incidents?</div>
<div class="check-item"><span class="checkbox"></span> Sleep pattern, appetite, mood?</div>
<div class="check-item"><span class="checkbox"></span> What activities do they participate in?</div>
<div class="check-item"><span class="checkbox"></span> Any medication changes since admission?</div>
<div class="check-item"><span class="checkbox"></span> Any concerns from the care team?</div>
<div class="check-item"><span class="checkbox"></span> Any concerns from <em>my</em> loved one I should know about?</div>

<h2>The 4-step escalation ladder</h2>
<p>
    When something is wrong, escalate in order. Skipping steps slows
    the process; following them gets a fix.
</p>

<h3>Step 1: Charge nurse (immediate, in person)</h3>
<p>
    For day-to-day care issues. Most problems stop here.
</p>

<h3>Step 2: Director of Nursing (within 24h)</h3>
<p>
    For clinical-level concerns the charge nurse can't or won't
    address. Email a follow-up to create a paper trail.
</p>

<h3>Step 3: Administrator (within 48h)</h3>
<p>
    For care-team accountability issues or pattern problems. Put it
    in writing; ask for a written response within 5 business days.
</p>

<h3>Step 4: State Long-Term Care Ombudsman</h3>
<p>
    Every state has one. Free, confidential, neutral. They
    investigate complaints and have real power. The phone number
    must be posted in every licensed facility. If you suspect
    abuse, neglect, or exploitation, you can also call Adult
    Protective Services in your state directly.
</p>

