    <div class="label">Bottom line</div>
    Done right, a small home is the closest thing to aging at home with
    real support. Done wrong, it's worse than the most over-marketed
    tower — because there's no scale to absorb a single bad caregiver.
    The questions in this guide separate one from the other.
</div>

