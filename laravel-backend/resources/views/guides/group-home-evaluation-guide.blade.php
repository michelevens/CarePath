<p class="lead">
    A group home or adult family home is a fundamentally different
    product from an assisted-living tower. Three to ten residents in a
    real house. Two or three caregivers who know everyone by name. No
    fitness center, no chef tasting menus — just a quieter, more
    domestic kind of care. This guide is how to evaluate one without
    being fooled by either the charm of the setting or the lack of
    polish.
</p>

<div class="callout">
    <div class="label">Who this guide is for</div>
    Families considering a small-setting (3-10 bed) residential option
    for a loved one — whether an older adult who finds large
    communities overstimulating, a person with dementia for whom a
    quieter environment reduces behavioral symptoms, or an adult with
    an intellectual or developmental disability (IDD) aging out of a
    family home into their own residence.
</div>

<h2>1. The small-setting alphabet soup</h2>
<p>
    The licensing label varies by state, but the model is consistent:
    a single-family-style home, owned or leased by a small operator,
    licensed to care for a capped number of residents.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Group Home</strong> — common Florida, Texas, Georgia term. Often regulated for IDD adults (Florida APD, Texas HHS). Capacity typically 4-15. Florida alone licenses ~2,200 of these.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Adult Family Home (AFH)</strong> — Washington, Oregon, Hawaii term. Up to 6 residents. Often the only Medicaid-friendly option in WA/OR.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Residential Care Facility for the Elderly (RCFE)</strong> — California's umbrella term. Includes both 6-bed homes and 200-bed communities. Look at capacity, not the label alone.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Personal Care Home</strong> — Pennsylvania, Georgia, others. 4-15 beds typical.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Board &amp; Care Home</strong> — older term, still used colloquially. Often refers to unlicensed or under-licensed homes — proceed carefully.</div>
<div class="check-item"><span class="checkbox"></span> <strong>ICF/IID</strong> — federally-regulated intermediate care facility for individuals with intellectual disabilities. Larger, more medical, separate funding stream from APD/state IDD waivers.</div>

<h2>2. When a small setting is the right answer</h2>
<p>
    The case for small-setting care is strongest when one or more of
    these fits:
</p>
<div class="check-item"><span class="checkbox"></span> Your loved one is <strong>overwhelmed by large communities</strong> — dining halls of 60, hallways of 200. A 6-bed home has one dining table.</div>
<div class="check-item"><span class="checkbox"></span> They have <strong>dementia and a history of wandering or sundowning</strong> — small homes can manage these behaviors with consistent staff, where large memory-care units rotate.</div>
<div class="check-item"><span class="checkbox"></span> They have an <strong>IDD diagnosis and the family is no longer able to provide round-the-clock care</strong> at home — group homes are usually the only option licensed for IDD adults.</div>
<div class="check-item"><span class="checkbox"></span> Budget is tight and your state's <strong>Medicaid HCBS waiver</strong> covers small-setting care but not assisted living. Florida, Washington, Oregon, and several others fall into this pattern.</div>
<div class="check-item"><span class="checkbox"></span> Your loved one was always a <strong>"home" person</strong> — never enjoyed cruises, hated dorm life, prefers small gatherings. They will not enjoy a 200-resident tower.</div>

<h2>3. The staffing math you have to ask about</h2>
<p>
    Small homes win on intimacy and lose on staffing redundancy. A
    6-bed AFH with 2 caregivers feels remarkable on tour day. On the
    third night shift in a row when one caregiver is sick, it can
    quickly become a single overworked person managing six residents.
    Ask these questions directly:
</p>
<div class="check-item"><span class="checkbox"></span> <strong>What's your day-shift caregiver-to-resident ratio? Night shift? Weekends?</strong></div>
<div class="check-item"><span class="checkbox"></span> <strong>Who covers when a caregiver calls out sick?</strong> Family of owner? Agency? Unfilled?</div>
<div class="check-item"><span class="checkbox"></span> <strong>Is the owner-operator on-site? How many hours per week?</strong> Owner-operated AFHs run very differently from absentee-owned ones.</div>
<div class="check-item"><span class="checkbox"></span> <strong>What's the turnover rate for caregivers in the last 12 months?</strong></div>
<div class="check-item"><span class="checkbox"></span> <strong>What licensing or training do caregivers have?</strong> CNA, Med-Tech, none?</div>
<div class="check-item"><span class="checkbox"></span> <strong>Who handles a 2am emergency?</strong> The night caregiver alone? An on-call nurse?</div>

<h2>4. The money picture — and the Medicaid path</h2>
<p>
    Small-setting prices typically run $4,000-$8,000/month private-pay
    nationally, with significant regional variation. The Medicaid
    story is the surprising one.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Most states' HCBS Medicaid waivers</strong> (Home and Community-Based Services) cover small-setting care at far higher rates than they cover traditional assisted living. Washington's COPES waiver, for example, pays AFHs roughly twice what it pays large ALs per resident.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Some states have IDD-specific waivers</strong> (Florida iBudget, California SDP, others) that pay group-home rates for adults with developmental disabilities, regardless of senior eligibility rules.</div>
<div class="check-item"><span class="checkbox"></span> <strong>The state-supplement angle</strong>: when an SSI recipient lives in a licensed small-setting home, many states (CA, MA, NY, others) pay a monthly supplement that closes the gap between SSI income and the home's room-and-board rate.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Ask the home directly</strong>: "What payer mix do you accept? Private pay, Medicaid waiver, state supplement, VA Aid &amp; Attendance, LTC insurance?" An honest home will give you a clean answer in 30 seconds.</div>

<div class="callout" style="margin-top: 8pt;">
    <div class="label">Watch out for: the "private-pay only for the first year" trap</div>
    Some small homes accept Medicaid waiver only after a year of private-pay
    residency. Get the spend-down policy in writing before you move in.
</div>

<h2>5. The tour-day reality check</h2>
<p>
    Small homes don't have model rooms or polished marketing teams.
    What you see is what you get. Use that.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Show up unannounced for a second visit.</strong> If they refuse, that's your answer. Most legitimate operators welcome it.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Eat a meal there if invited.</strong> The kitchen tells the story — chef-prepared marketing copy is irrelevant when there are five other residents at the table.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Watch one resident-caregiver interaction for 60 seconds.</strong> Is the caregiver addressing the resident by name, eye-level, with patience? Or yelling instructions from across the room?</div>
<div class="check-item"><span class="checkbox"></span> <strong>Look at the residents themselves.</strong> Are they dressed? Hair brushed? Engaged in something, or all parked in front of a TV? Smells clean or smells of urine?</div>
<div class="check-item"><span class="checkbox"></span> <strong>Ask to see the most recent licensing inspection report.</strong> Every licensed home in every state has one. Refusal = walk.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Get the names of two current resident families.</strong> Call them yourself. Ask the questions you can't ask the operator.</div>

<h2>6. Red flags specific to small homes</h2>
<div class="check-item"><span class="checkbox"></span> Cash-only payment, no contract, no invoice</div>
<div class="check-item"><span class="checkbox"></span> Owner won't disclose their license number or it can't be found in the state directory</div>
<div class="check-item"><span class="checkbox"></span> All residents look the same age and same condition — diversity of acuity is normal; uniformity often signals over-promising</div>
<div class="check-item"><span class="checkbox"></span> A resident answers the door, not staff</div>
<div class="check-item"><span class="checkbox"></span> "We don't do background checks because we know everyone"</div>
<div class="check-item"><span class="checkbox"></span> Discharge clause that takes effect on any hospitalization (forces you to move during the worst possible week)</div>
<div class="check-item"><span class="checkbox"></span> The home is also someone's personal residence and you can't tell which rooms are which</div>

<h2>7. Questions to ask the state, not the home</h2>
<p>
    Every state publishes some version of the following — call or
    look up before you sign anything.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>License status</strong> — current, expired, conditional, denied?</div>
<div class="check-item"><span class="checkbox"></span> <strong>Recent inspection deficiencies</strong> — what was cited and what's been remediated?</div>
<div class="check-item"><span class="checkbox"></span> <strong>Substantiated complaints</strong> in the last 2 years</div>
<div class="check-item"><span class="checkbox"></span> <strong>Owner / administrator history</strong> — have they operated other homes? Were any closed or sanctioned?</div>

