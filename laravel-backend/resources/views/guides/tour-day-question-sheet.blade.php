<p class="lead">
    Bring this with you to every facility tour. Print one copy per facility. Score each answer in your own shorthand — facilities will start to look alike after the third tour, and your notes are what you'll come back to.
</p>

<div class="callout">
    <div class="label">How to use this</div>
    Don't try to ask all 47. Pick the 10 that matter most for your loved one's situation. Use the rest only if the conversation slows down or you want to dig into a specific area (e.g., memory care, behavior management, end-of-life).
</div>

<h2>1. Level of care &amp; medical</h2>
<div class="check-item"><span class="checkbox"></span> What levels of care do you offer, and what triggers a level change?</div>
<div class="check-item"><span class="checkbox"></span> If my loved one's needs grow, do they have to move? Where?</div>
<div class="check-item"><span class="checkbox"></span> How are medications managed — self, staff-administered, eMAR?</div>
<div class="check-item"><span class="checkbox"></span> Who handles wound care, IVs, oxygen? Are these extra?</div>
<div class="check-item"><span class="checkbox"></span> Is there a doctor on-site? How often? Is hospice allowed?</div>
<div class="check-item"><span class="checkbox"></span> What's your hospital readmission rate?</div>
<div class="check-item"><span class="checkbox"></span> How do you handle behavioral changes, sundowning, or wandering?</div>

<h2>2. Staffing</h2>
<div class="check-item"><span class="checkbox"></span> What's your nurse-to-resident ratio on day shift? Night shift? Weekends?</div>
<div class="check-item"><span class="checkbox"></span> Is an RN on-site 24/7, or on-call after hours?</div>
<div class="check-item"><span class="checkbox"></span> What's your annual staff turnover percentage?</div>
<div class="check-item"><span class="checkbox"></span> How long has the administrator been here? The director of nursing?</div>
<div class="check-item"><span class="checkbox"></span> Do you use agency / temp staff? How often?</div>
<div class="check-item"><span class="checkbox"></span> What training do CNAs and med-techs receive on memory care / dementia?</div>

<h2>3. Money</h2>
<div class="check-item"><span class="checkbox"></span> What's the all-in monthly cost for my loved one's level of care?</div>
<div class="check-item"><span class="checkbox"></span> What's <em>not</em> included? Itemize: meds, supplies, beauty, laundry, cable.</div>
<div class="check-item"><span class="checkbox"></span> What's your annual rate increase history (last 3 years)?</div>
<div class="check-item"><span class="checkbox"></span> What's the community fee / move-in fee, and is it refundable?</div>
<div class="check-item"><span class="checkbox"></span> Do you accept Medicaid? Long-term care insurance? VA Aid &amp; Attendance?</div>
<div class="check-item"><span class="checkbox"></span> If we run out of private funds, do we have to leave?</div>
<div class="check-item"><span class="checkbox"></span> Can I see a sample contract before signing?</div>

<h2>4. Safety &amp; the building</h2>
<div class="check-item"><span class="checkbox"></span> When was your last state inspection? Any deficiencies? May I see the report?</div>
<div class="check-item"><span class="checkbox"></span> What's your fall-prevention program?</div>
<div class="check-item"><span class="checkbox"></span> How are call lights answered? What's the average response time?</div>
<div class="check-item"><span class="checkbox"></span> Is the memory-care unit secured? What does that mean physically?</div>
<div class="check-item"><span class="checkbox"></span> Sprinklers, generators, evacuation plan — what's in place?</div>

<h2>5. Daily life</h2>
<div class="check-item"><span class="checkbox"></span> Can I see the actual room (or a similar one), not just the model?</div>
<div class="check-item"><span class="checkbox"></span> Can residents bring their own furniture? Pets?</div>
<div class="check-item"><span class="checkbox"></span> What's a typical day's schedule?</div>
<div class="check-item"><span class="checkbox"></span> May I see this week's activities calendar?</div>
<div class="check-item"><span class="checkbox"></span> How are residents matched with care assistants — same person each day?</div>

<h2>6. Food</h2>
<div class="check-item"><span class="checkbox"></span> Can I eat lunch with the residents today?</div>
<div class="check-item"><span class="checkbox"></span> How are special diets handled — diabetic, low-sodium, gluten-free, pureed?</div>
<div class="check-item"><span class="checkbox"></span> Can residents request specific meals or eat off-menu?</div>
<div class="check-item"><span class="checkbox"></span> Where do meals come from — in-house kitchen or contracted?</div>

<h2>7. Family experience</h2>
<div class="check-item"><span class="checkbox"></span> How are families kept informed about changes in care or condition?</div>
<div class="check-item"><span class="checkbox"></span> What's your visiting policy? Quiet hours?</div>
<div class="check-item"><span class="checkbox"></span> How do you handle complaints? Who do I call?</div>
<div class="check-item"><span class="checkbox"></span> Can I speak to a current resident's family? (Always ask. Always.)</div>

<h2>8. End of life &amp; transitions</h2>
<div class="check-item"><span class="checkbox"></span> Is end-of-life care available on-site, or must residents be moved?</div>
<div class="check-item"><span class="checkbox"></span> Do you allow outside hospice providers?</div>
<div class="check-item"><span class="checkbox"></span> What are the conditions under which you would discharge a resident?</div>
<div class="check-item"><span class="checkbox"></span> What's your notice period if you can no longer care for someone?</div>

<h2>9. Tour-day vibe check</h2>
<div class="check-item"><span class="checkbox"></span> What does it <em>smell</em> like at 11am? At 3pm?</div>
<div class="check-item"><span class="checkbox"></span> Are residents engaged or parked in front of a TV?</div>
<div class="check-item"><span class="checkbox"></span> Do staff greet residents by name? Do residents greet staff back?</div>
<div class="check-item"><span class="checkbox"></span> Does the place feel like a home or a hospital?</div>
<div class="check-item"><span class="checkbox"></span> Would you feel comfortable having dinner here yourself?</div>
<div class="check-item"><span class="checkbox"></span> What did your gut tell you in the first 30 seconds?</div>

<div class="callout" style="margin-top: 22pt;">
    <div class="label">Red flags to walk on</div>
    Strong urine smell · staff who can't answer basic care questions · refusal to share inspection reports · residents who look unattended · staff yelling or rushing · pricing that keeps changing during the tour · pressure to sign today.
</div>

