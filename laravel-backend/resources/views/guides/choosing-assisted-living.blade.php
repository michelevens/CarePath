<h2>Step 1 — Honest needs assessment</h2>
<p>Before you tour, write down what your loved one actually needs. Many families pick the wrong level of care because they're optimistic about today and pessimistic about tomorrow.</p>
<h3>Care needs</h3>
<ul>
    <li>Which ADLs need help today? (Bathing, dressing, eating, toileting, transferring, walking)</li>
    <li>Memory: occasional forgetfulness, repeating questions, getting lost, wandering, sundowning?</li>
    <li>Medications: how many, how complex, can they self-administer?</li>
    <li>Continence: occasional accidents, frequent, full incontinence?</li>
    <li>Mobility: walks freely, uses a walker, wheelchair, transfers with help, bedbound?</li>
    <li>Behaviors: any aggression, refusal of care, exit-seeking?</li>
</ul>
<h3>Trajectory</h3>
<p>What's likely 12 months from now? 24? If memory issues are progressing, a memory-care unit (or a facility with one on-site) may save a disruptive move later.</p>

