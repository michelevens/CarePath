<h2>Step 3 — Pick the right geography</h2>
<p>Two competing forces — pick which matters more:</p>
<ul>
    <li><strong>Close to family</strong>: visits will be more frequent, advocacy is easier, transitions are smoother. The single best predictor of resident wellbeing is family visit frequency.</li>
    <li><strong>Close to current home / preferred area</strong>: keeps familiar surroundings, doctors, friends, place of worship. Reduces move trauma.</li>
</ul>
<p>If family lives in a different metro, biting the bullet and moving the loved one near family typically wins long-term, despite the upfront disruption.</p>

<h2>Step 4 — Filter by certification &amp; quality</h2>
<ul>
    <li><strong>State license</strong> — every legitimate facility has one. Verify on your state's licensure portal.</li>
    <li><strong>CMS Five-Star rating</strong> — for skilled nursing; not directly applicable to pure assisted living but the parent organization's SNF ratings are a useful proxy.</li>
    <li><strong>State inspection report</strong> — request the last 2 inspection reports. Look for deficiency patterns (repeated medication errors, repeated fall incidents, repeated abuse/neglect findings).</li>
    <li><strong>Ownership</strong> — for-profit, non-profit, or government? Each can deliver excellent care; ownership is one signal among many. Recent ownership change is worth knowing about.</li>
    <li><strong>Accreditations</strong> — CARF, Joint Commission, or state-specific (e.g., Assisted Living Federation). Not required but signals process maturity.</li>
</ul>

<h2>Step 5 — Tour with structure</h2>
<p>Tour at <em>different times of day</em> — a Tuesday at 11am looks different from a Saturday at 3pm. Schedule the second tour without an appointment if possible. Things to do on every tour:</p>
<ul>
    <li>Eat a meal there (insist; never decline this).</li>
    <li>Use the bathroom in the common area. Look for cleanliness and call-cord reach.</li>
    <li>Walk a hall without the sales director — observe how staff interact with residents.</li>
    <li>Sit in the lobby for 15 minutes afterward. Listen.</li>
    <li>Use CarePath's "Tour Day: 47 Questions" guide; bring it printed.</li>
</ul>

<h2>Step 6 — Verify references</h2>
<p>Ask the facility for 3 family references. Then go outside that list:</p>
<ul>
    <li>Search the facility name in your state's <strong>ombudsman complaint database</strong> (long-term care ombudsman program — every state has one, free).</li>
    <li>Ask the local <strong>Area Agency on Aging</strong> — they often know facility reputations.</li>
    <li>Read online reviews (CarePath, Caring.com, Google) — discount the most glowing and most furious; look for patterns in the middle.</li>
    <li>If you know any nurses or social workers in your area, ask them. Insiders have opinions.</li>
</ul>

<h2>Step 7 — Read the contract before you sign</h2>
<p>Ask for a sample contract before you tour, not after you've decided. Get an elder-law attorney to review it (typically $200–$500 — well worth it). Watch for:</p>
<ul>
    <li><strong>Discharge clauses</strong> — what behaviors or care needs trigger eviction? How much notice? Where do they help you go?</li>
    <li><strong>Rate increase clauses</strong> — how much notice for rate increases? Any cap?</li>
    <li><strong>Refund of community fee</strong> — under what conditions? Pro-rated by month?</li>
    <li><strong>Mandatory arbitration</strong> — most contracts have it. You can usually negotiate it out, but the facility may not always agree.</li>
    <li><strong>Power of attorney requirements</strong> — they'll require a documented POA. Have it ready.</li>
    <li><strong>"Hold" policy</strong> — if your loved one is hospitalized, how long do they hold the room, at what cost?</li>
</ul>

<h2>Step 8 — Plan the payment runway</h2>
<p>Decide today what you'll do in month 25, 36, or 60 if private funds run low:</p>
<ul>
    <li>Will the facility accept Medicaid then? (Most assisted living does NOT.)</li>
    <li>If not, where would your loved one move? Tour those facilities now, before you need them.</li>
    <li>If LTC insurance applies, understand the elimination period (most are 90 days), benefit cap, daily maximum.</li>
    <li>If VA Aid &amp; Attendance applies, apply now — benefits are retroactive to application date, but processing takes 3–8 months.</li>
</ul>

<h2>Step 9 — Plan the transition</h2>
<ul>
    <li>Visit the room ahead of move-in; decide which furniture and meaningful items will come</li>
    <li>Bring familiar items first: photos, blankets, a favorite chair</li>
    <li>Notify the loved one's primary care doctor; coordinate medication list and records transfer</li>
    <li>Update mail forwarding, banks, pharmacies, Medicare contact info</li>
    <li>Stop or transfer any home services (internet, utilities, lawn)</li>
    <li>Plan visits for the first 2 weeks — daily if possible. The first 30 days are the hardest.</li>
    <li>Don't take early calls home ("I want to leave") as the final verdict; most residents settle in by week 4–6.</li>
</ul>

<div class="callout">
    <div class="label">The 3-finalist rule</div>
    Touring 8 facilities is exhausting and starts to blur. Use Steps 1–4 to filter your list down to 3 finalists, then tour deeply (twice each, including a meal). Three apples-to-apples comparisons beat ten superficial ones.
</div>

<h2>Red flags that should end the conversation</h2>
<ul>
    <li>Strong urine smell on multiple visits at multiple times</li>
    <li>Residents who appear unattended or call-lights ringing unanswered</li>
    <li>Staff who can't or won't share inspection reports</li>
    <li>Pricing that keeps changing during the tour</li>
    <li>Pressure to sign or put down a deposit today</li>
    <li>Refusal to share a sample contract for outside review</li>
    <li>No clear answer about who handles care after hours</li>
    <li>High visible staff turnover or use of agency staff for primary caregivers</li>
</ul>

