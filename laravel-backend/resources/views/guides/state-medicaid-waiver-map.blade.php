@section('content')

<p class="lead">
    Traditional Medicaid pays for nursing-home care almost everywhere
    in the U.S. — but for the more affordable, more flexible
    alternatives (assisted living, group homes, in-home care), payment
    runs through a different door: <strong>Home and Community-Based
    Services (HCBS) waivers</strong>. Each state runs its own. This is
    the cheat sheet to find yours, what it covers, and how long the
    line is.
</p>

<div class="callout">
    <div class="label">Why waivers exist (the short version)</div>
    Medicaid's nursing-home entitlement is open-ended — if you qualify,
    you're covered. HCBS waivers are <em>capped</em>: states get
    federal permission to "waive" the institutional-care requirement
    and pay for community care instead, but only for a fixed number of
    people. That's why waiting lists exist — and why timing your
    application matters.
</div>

<h2>1. What a typical HCBS waiver covers</h2>
<p>
    Coverage varies, but most senior-aged or aged/disabled waivers
    pay for some combination of:
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Personal care</strong> in your own home (bathing, dressing, meal prep)</div>
<div class="check-item"><span class="checkbox"></span> <strong>Assisted-living facility</strong> room-and-board, or just the care portion (varies by state)</div>
<div class="check-item"><span class="checkbox"></span> <strong>Adult day health programs</strong></div>
<div class="check-item"><span class="checkbox"></span> <strong>Respite care</strong> for family caregivers</div>
<div class="check-item"><span class="checkbox"></span> <strong>Home modifications</strong> (ramps, grab bars, walk-in showers)</div>
<div class="check-item"><span class="checkbox"></span> <strong>Personal emergency response systems</strong> (button pendants)</div>
<div class="check-item"><span class="checkbox"></span> <strong>Care coordination / case management</strong></div>
<div class="check-item"><span class="checkbox"></span> <strong>Transportation</strong> to medical appointments</div>

<p style="margin-top: 14pt;">
    What waivers almost never cover: room-and-board in a facility
    setting (you or SSI pays that part), prescription drugs (those
    go through regular Medicaid or Medicare Part D), and skilled
    nursing care (that's still a SNF / institutional Medicaid path).
</p>

<h2>2. The 50-state landscape, roughly grouped</h2>

<h3>Waivers with no waiting list, or very short ones</h3>
<p>
    These are the states where waivers function more like an
    entitlement — if you qualify, you're in within weeks.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>California</strong> — Assisted Living Waiver (ALW); Multipurpose Senior Services Program (MSSP); In-Home Supportive Services (IHSS, technically a State Plan service)</div>
<div class="check-item"><span class="checkbox"></span> <strong>Washington</strong> — COPES (Community Options Program Entry System); New Freedom; Residential Support Waiver</div>
<div class="check-item"><span class="checkbox"></span> <strong>Oregon</strong> — K Plan / Oregon Project Independence; APD HCBS</div>
<div class="check-item"><span class="checkbox"></span> <strong>Minnesota</strong> — Elderly Waiver (EW); Community Access for Disability Inclusion</div>
<div class="check-item"><span class="checkbox"></span> <strong>Vermont</strong> — Choices for Care (uniquely runs as a managed-care entitlement, no waitlist for the highest-need tier)</div>

<h3>Waivers with significant waiting lists (1-5+ years)</h3>
<p>
    File anyway. Most states honor your application date — the longer
    you wait to file, the longer your eventual wait.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Florida</strong> — Statewide Medicaid Managed Care Long-Term Care (SMMC-LTC). 5-15 year waitlist for non-Medicaid-pending applicants; expedited if currently in nursing-home placement.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Texas</strong> — STAR+PLUS HCBS waiver. Interest list (waitlist) of 6-10 years typical.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Georgia</strong> — CCSP (Community Care Services Program); SOURCE.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Tennessee</strong> — CHOICES program. Three groups, different waitlists.</div>
<div class="check-item"><span class="checkbox"></span> <strong>South Carolina</strong> — Community Choices Waiver.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Pennsylvania</strong> — Community HealthChoices (CHC); OPTIONS Program.</div>

<h3>Waivers that pay for assisted living specifically</h3>
<p>
    Not all states' HCBS waivers cover ALF care — these are the ones
    that do, with a defined ALF benefit:
</p>
<div class="check-item"><span class="checkbox"></span> California (ALW), Florida (SMMC-LTC), Massachusetts (Frail Elder Waiver), Minnesota (EW), New Jersey (Managed Long-Term Services and Supports), New York (Assisted Living Program), North Carolina (Community Alternatives Program), Oregon, Texas (STAR+PLUS HCBS), Washington (COPES), Wisconsin (Family Care).</div>

<h3>IDD-specific waivers (separate from senior waivers)</h3>
<p>
    For adults with intellectual or developmental disabilities, every
    state runs a distinct waiver track — usually with longer
    waitlists than the senior waivers because demand is concentrated:
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Florida iBudget Waiver</strong> (APD-administered) — 22,000+ on the waitlist; "Crisis Status" expedites.</div>
<div class="check-item"><span class="checkbox"></span> <strong>California Self-Determination Program (SDP)</strong> — wraparound DDS regional center services.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Texas HCS</strong> (Home and Community-based Services) — interest list 12+ years statewide.</div>

<h2>3. Who qualifies (the three filters)</h2>
<p>
    Every state applies the same three filters. The thresholds vary;
    the categories don't.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Functional eligibility</strong> — you must need a nursing-home level of care, measured by ADLs (activities of daily living: bathing, dressing, toileting, transferring, eating). Typically 2-3 ADL impairments minimum.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Financial eligibility — income</strong> — usually 300% of the SSI federal benefit rate ($2,829/mo single in 2026). States with "medically needy" pathways waive this with a spend-down.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Financial eligibility — assets</strong> — typically $2,000 for an individual, $3,000 for a couple, with the marital home and one car exempt. Spousal impoverishment rules protect the spouse who isn't applying.</div>

<h2>4. How to apply (the universal steps)</h2>
<div class="check-item"><span class="checkbox"></span> <strong>Step 1:</strong> Find your state's Aging &amp; Disability Resource Center (ADRC) — every state has one. They're the no-cost, neutral starting point.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Step 2:</strong> Request a Level-of-Care (LOC) assessment. A nurse or social worker comes to your home, scores you against the state's ADL/nursing-need rubric. Free.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Step 3:</strong> If functionally eligible, apply to your state Medicaid agency for financial eligibility. This is where assets and income are scrutinized — the 5-year look-back applies.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Step 4:</strong> Get added to the waiver's enrollment list. If there's a waitlist, your application date is your priority date.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Step 5:</strong> When a slot opens, you choose providers — home-care agency, ALF, group home — and the case manager authorizes services.</div>

<div class="callout" style="margin-top: 8pt;">
    <div class="label">The "I didn't know I qualified" tax</div>
    Most families discover HCBS waivers only after a placement
    decision is already made. Filing the application years ahead of
    actual need — even speculatively — costs nothing and can save
    $50-150k in eventual private-pay outlays.
</div>

<h2>5. Where to look up your state's waiver</h2>
<p>
    Every state's Medicaid agency publishes its current waivers. Two
    fast paths:
</p>
<div class="check-item"><span class="checkbox"></span> <strong>medicaid.gov</strong> — federal site. Search "[your state] HCBS waivers" or use the State Overviews page.</div>
<div class="check-item"><span class="checkbox"></span> <strong>n4a.org</strong> — National Association of Area Agencies on Aging. Their "Eldercare Locator" connects you to your local ADRC, the people who can actually walk you through the application.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Your state Medicaid agency website</strong> — search "[your state] Medicaid long-term care waiver". Every state has a dedicated landing page.</div>
<div class="check-item"><span class="checkbox"></span> <strong>An elder-law attorney</strong> — worth the consultation fee for complex cases (significant assets, look-back concerns, spousal income protection).</div>

<div class="callout" style="margin-top: 18pt; background:#F4F4F2;">
    <div class="label">The one thing to do this week</div>
    Call your state's Aging &amp; Disability Resource Center (ADRC) and
    ask for the current HCBS waiver waitlist status. If there's a list,
    file the application now even if you don't expect to need services
    for years. Your application date is your priority.
</div>

