@section('content')

<p class="lead">
    Most families who hold a long-term care insurance policy never
    file the claim. They pay premiums for 20+ years, then quietly
    burn $50,000-100,000 of their own money on care that the policy
    would have covered. The barrier is rarely the policy — it's the
    paperwork and the timing. This guide is the step-by-step play.
</p>

<div class="callout">
    <div class="label">If this applies to you</div>
    Your loved one (or you) bought a long-term care insurance policy
    at some point — typically in the late 1990s or 2000s. The policy
    has a daily benefit ($100-300/day is typical), a benefit pool
    (total dollars or years of coverage), and an elimination period
    (the deductible measured in days). This guide is for actually
    activating that policy now that care is needed.
</div>

<h2>1. Find the policy first</h2>
<p>
    The biggest failure mode is families who can't locate the
    policy. By the time placement is needed, paperwork is often
    decades old.
</p>
<div class="check-item"><span class="checkbox"></span> Check the loved one's filing cabinet for "long-term care insurance," "Genworth," "John Hancock," "MetLife," "MassMutual," "Mutual of Omaha," "Northwestern Mutual," "TransAmerica," "Unum" — the major issuers</div>
<div class="check-item"><span class="checkbox"></span> Look at past tax returns — premiums may show as itemized medical deductions</div>
<div class="check-item"><span class="checkbox"></span> Bank statements — premium payments are typically monthly or annual ACH</div>
<div class="check-item"><span class="checkbox"></span> Their financial advisor or estate attorney may have a copy</div>
<div class="check-item"><span class="checkbox"></span> The National Association of Insurance Commissioners (NAIC) maintains a Life Insurance Policy Locator at naic.org — for orphan policies where the issuer is unknown</div>

<h2>2. Read the four numbers that matter</h2>
<p>
    Skip the small print initially. Find these four numbers from
    the policy declarations page:
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Daily benefit amount</strong> — e.g. $200/day. This is the maximum the policy pays per day.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Benefit pool</strong> — either total dollars (e.g. $240,000) or years (e.g. 3-year benefit). Once you've drawn this down, the policy stops.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Elimination period</strong> — number of days you pay out of pocket before the policy starts paying. 30, 60, 90 days is typical. The clock starts when the loved one becomes benefit-eligible, not when you file.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Inflation rider</strong> — does the daily benefit increase 3-5% annually? Policies from the early 2000s often have generous riders; the daily benefit may now be 2x the original.</div>

<h2>3. The eligibility threshold (this is the gate)</h2>
<p>
    Every policy defines "benefit-eligible" the same way: the
    insured must be unable to perform 2 of 6 Activities of Daily
    Living (ADLs) without assistance, OR have a cognitive
    impairment requiring substantial supervision.
</p>
<p><strong>The 6 ADLs:</strong></p>
<div class="check-item"><span class="checkbox"></span> Bathing</div>
<div class="check-item"><span class="checkbox"></span> Dressing</div>
<div class="check-item"><span class="checkbox"></span> Toileting</div>
<div class="check-item"><span class="checkbox"></span> Transferring (in and out of bed/chair)</div>
<div class="check-item"><span class="checkbox"></span> Continence</div>
<div class="check-item"><span class="checkbox"></span> Eating</div>

<div class="callout" style="margin-top: 8pt;">
    <div class="label">Why families get denied initially</div>
    Insurers want documented evidence, not the family's word. The
    physician must assess and document ADL deficits in clear,
    insurer-speak. "Mom needs help bathing because of her arthritis"
    won't trigger eligibility. "Mom requires hands-on assistance
    with bathing due to inability to safely transfer in and out of
    the tub; cannot complete the task independently" will.
</div>

<h2>4. The 5-step claim process</h2>

<h3>Step 1: Call the issuer and request the claim packet</h3>
<p>
    Every issuer has a claims phone line. Tell them you're
    initiating a claim. They send you a packet — usually 20-30 pages
    of forms.
</p>

<h3>Step 2: Complete the policyholder section</h3>
<p>
    Name, address, current condition summary. Have the
    policyholder sign (or POA on their behalf — your POA document
    must be on file).
</p>

<h3>Step 3: Get the physician's section completed</h3>
<p>
    The doctor's portion is where claims live or die. Most
    physicians have NEVER seen one of these forms. Coach them:
</p>
<div class="check-item"><span class="checkbox"></span> Ask for an appointment specifically to complete LTC insurance paperwork — 15-30 minutes</div>
<div class="check-item"><span class="checkbox"></span> Bring the form printed out, along with a brief 1-page summary of the loved one's care needs in clear ADL language</div>
<div class="check-item"><span class="checkbox"></span> Be specific: "Hands-on assistance," not "reminders." "Cannot," not "has difficulty." "Daily," not "sometimes."</div>
<div class="check-item"><span class="checkbox"></span> If cognitive impairment is the basis, the physician should document the diagnosis (Alzheimer's, vascular dementia, etc.), MMSE/MoCA score, and the specific safety risks of being alone</div>

<h3>Step 4: Submit + track the elimination period</h3>
<p>
    Mail / fax / portal-upload the completed packet. The insurer
    will assign a claim number and start the elimination-period
    clock from your earliest documented benefit-eligibility date.
</p>
<div class="check-item"><span class="checkbox"></span> Keep copies of EVERYTHING you submit</div>
<div class="check-item"><span class="checkbox"></span> Get a claim number and the claims adjuster's direct line</div>
<div class="check-item"><span class="checkbox"></span> Begin documenting daily care expenses — receipts, invoices, caregiver timesheets</div>
<div class="check-item"><span class="checkbox"></span> Track the elimination-period clock — note the date care started, the date elimination is satisfied</div>

<h3>Step 5: Submit ongoing reimbursement requests</h3>
<p>
    Once the elimination period is met, the policy pays. Most
    policies are <strong>reimbursement-based</strong> — you pay the
    facility, then submit receipts monthly for reimbursement, up to
    the daily benefit amount. Some newer policies are
    <strong>indemnity-based</strong> — they cut a check for the
    daily benefit regardless of actual cost. Check which yours is.
</p>

<h2>5. The four mistakes that delay or kill claims</h2>
<div class="check-item"><span class="checkbox"></span> <strong>Filing too early.</strong> If the loved one isn't yet 2-ADL-deficient or cognitively impaired enough to need substantial supervision, the claim will be denied and the file flagged. Wait until the clinical picture clearly meets eligibility.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Filing too late.</strong> Conversely, many policies have a notification requirement (you must notify within X days of becoming benefit-eligible). Failing to notify can void retroactive coverage.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Vague physician documentation.</strong> "Patient seems to struggle with daily tasks" is not a benefit-eligibility statement. Specifics matter.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Trying to do it without POA.</strong> If the policyholder has cognitive impairment, the family member managing the claim needs a current durable POA on file with the insurer.</div>

<h2>6. If you're denied</h2>
<p>
    Initial denials are common — sometimes 30-40% of LTC claims are
    denied the first time. Most reverse on appeal with better
    documentation.
</p>
<div class="check-item"><span class="checkbox"></span> Request the specific reason for denial in writing</div>
<div class="check-item"><span class="checkbox"></span> Address the specific deficiency (usually clinical documentation)</div>
<div class="check-item"><span class="checkbox"></span> Get a second physician assessment if the first was vague</div>
<div class="check-item"><span class="checkbox"></span> Submit an internal appeal — most insurers give you 60-180 days</div>
<div class="check-item"><span class="checkbox"></span> If internal appeal fails, file a complaint with your state insurance commissioner — this often moves things faster than continued internal review</div>
<div class="check-item"><span class="checkbox"></span> Last resort: hire an LTC claim attorney. They typically take a percentage of recovered benefits; some work on contingency</div>

<div class="callout" style="margin-top: 18pt; background:#F4F4F2;">
    <div class="label">Bottom line</div>
    The policy isn't a piggy bank — but it's also not a death pact.
    For a family with a paid-up policy and a loved one entering
    care, properly filing the claim is worth $50,000-150,000 over
    the policy's payout window. The 4-6 hours it takes to do this
    well is the highest-leverage paperwork a family caregiver will
    ever do.
</div>

