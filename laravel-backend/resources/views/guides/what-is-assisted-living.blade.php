<p class="lead">
    Assisted living is the level of care for older adults who need help with daily life but not full-time medical supervision. This is the plain-English introduction families wish they'd had before their first tour.
</p>

<h2>What it is</h2>
<p>An <strong>Assisted Living Facility (ALF)</strong> is a residential community where seniors live in their own apartment or room, eat meals in a shared dining room, take part in activities, and receive help with the daily tasks they can't (or shouldn't) handle alone — bathing, dressing, medication management, light housekeeping, transportation.</p>
<p>It's regulated at the state level (not federal like nursing homes), so rules and naming vary. You may hear: Assisted Living, Personal Care Home, Residential Care Facility for the Elderly (RCFE), Adult Care Home, Board and Care. They're cousins.</p>

<h2>Who it's for</h2>
<p>The right candidate is someone who:</p>
<ul>
    <li>Needs help with <strong>2 or more activities of daily living</strong> (ADLs) — bathing, dressing, eating, toileting, transferring, walking</li>
    <li>Is no longer safe living alone (medication errors, falls, food spoiling, getting lost)</li>
    <li>Is socially isolated and would benefit from a community</li>
    <li>Does <strong>not</strong> need 24/7 skilled medical care</li>
    <li>Is generally able to make decisions about their day, even if they need reminders</li>
</ul>

<h2>What's typically included in the monthly cost</h2>
<ul>
    <li>Private or semi-private apartment / room</li>
    <li>3 meals/day plus snacks</li>
    <li>Help with personal care (level depends on assessment)</li>
    <li>Medication management</li>
    <li>Housekeeping and laundry</li>
    <li>Scheduled activities and social programs</li>
    <li>Transportation to medical appointments (often within a radius)</li>
    <li>24-hour staff on-site</li>
    <li>Emergency response system in the room</li>
</ul>

<h2>What's usually NOT included (extra charges)</h2>
<ul>
    <li>Skilled nursing services (IVs, wound care, oxygen management)</li>
    <li>Specialized memory-care unit (typically 30–50% premium)</li>
    <li>Salon services, manicures</li>
    <li>Cable, internet, phone (sometimes)</li>
    <li>Outings beyond medical transportation</li>
    <li>Incontinence supplies</li>
    <li>Medications themselves (the pharmacy bill)</li>
</ul>

