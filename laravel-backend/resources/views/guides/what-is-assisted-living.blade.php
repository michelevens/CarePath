<h2>The pricing model</h2>
<p>Most assisted living uses a <strong>base rate + level-of-care adders</strong> model:</p>
<ul>
    <li><strong>Base rate</strong> covers the apartment, meals, and a minimum level of care.</li>
    <li><strong>Level-of-care adder</strong> is added based on a points-based assessment — more help needed = higher adder.</li>
    <li><strong>Community fee</strong> is a one-time move-in fee, often $1,500–$5,000, sometimes refundable in part.</li>
    <li><strong>Ancillary fees</strong> for incontinence supplies, beauty salon, extra transportation, etc.</li>
</ul>

<div class="callout">
    <div class="label">The reassessment trap</div>
    Care needs and care levels are reassessed quarterly (or sooner if things change). When level goes up, monthly cost goes up — sometimes by hundreds or thousands per month. Always ask facilities to walk you through their assessment scoring system on the tour, and what each level costs.
</div>

<h2>How people pay</h2>
<ul>
    <li><strong>Private pay</strong> — savings, retirement income, home sale. The default; most assisted living is funded this way.</li>
    <li><strong>Long-term care insurance</strong> — if purchased years ago, often covers assisted living (read the policy for ADL triggers and waiting periods).</li>
    <li><strong>VA Aid &amp; Attendance</strong> — up to $2,727/mo (single vet) or $3,232/mo (vet + spouse) for those who served during wartime. See the VA guide.</li>
    <li><strong>Medicaid HCBS waivers</strong> — limited availability; varies dramatically by state and waitlist. Many ALFs do not accept Medicaid.</li>
    <li><strong>Medicare</strong> — does <em>not</em> cover assisted living. Don't be misled by sales materials.</li>
    <li><strong>Reverse mortgages, life-settlement of life insurance, bridge loans</strong> — last-resort tactics; talk to a financial planner first.</li>
</ul>

<h2>How long the average stay lasts</h2>
<p>Median assisted living stay is around <strong>22 months</strong>. Many residents transition to memory care (if cognition declines) or skilled nursing (if medical needs increase). About 60% of ALF residents eventually move to a higher level of care.</p>

<h2>When assisted living is NOT the right fit</h2>
<ul>
    <li><strong>Needs 24/7 RN-level medical care</strong> → skilled nursing is required</li>
    <li><strong>Moderate-to-severe dementia with wandering or behaviors</strong> → memory care unit (secured)</li>
    <li><strong>Fully independent and primarily wants amenities</strong> → independent living is a better fit and far cheaper</li>
    <li><strong>Refuses any help and is still safe alone</strong> → in-home care may bridge the gap longer</li>
</ul>

<div class="callout">
    <div class="label">The "right time" question</div>
    The most common regret families share is waiting too long. Signs it's time: unexplained weight loss, more frequent falls, mail piling up, food spoiling, missed medications, isolation, repeated hospitalizations. Touring early — even before you think you need to — gives you choices later, when crises don't allow shopping around.
</div>

