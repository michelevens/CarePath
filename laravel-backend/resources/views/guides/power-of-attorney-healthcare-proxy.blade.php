@section('content')

<p class="lead">
    The single most useful piece of paper a family caregiver can hold
    is a signed, valid power of attorney for the person they're caring
    for — paired with a healthcare proxy, a living will, and (in many
    states) a POLST form. Get them set up before they're needed. Once
    cognitive capacity is gone, the only path left is guardianship,
    and that path is slow, expensive, and adversarial.
</p>

<div class="callout">
    <div class="label">A clear-eyed warning</div>
    This guide explains the categories of documents, what each does,
    and how they're typically executed. It is <strong>not legal
    advice</strong>. Your state's specific forms and requirements
    matter — a $300 elder-law consultation to validate yours is
    almost always worth it.
</div>

<h2>1. The four documents (and what each one does)</h2>

<h3>Financial / General Power of Attorney</h3>
<p>
    Authorizes someone (the "agent" or "attorney-in-fact") to make
    financial and legal decisions on the principal's behalf — pay
    bills, sign checks, manage accounts, sell property, file taxes,
    contract with a facility.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Durable</strong> means it remains valid after the principal loses capacity. <em>Always make it durable.</em> A non-durable POA evaporates exactly when you need it most.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Springing</strong> means it only activates upon a specified event (usually a physician's certification of incapacity). Sounds prudent, often a problem in practice — banks frequently refuse springing POAs until shown formal capacity findings.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Immediate</strong> means it's effective the moment the principal signs. The simpler path, the path most elder-law attorneys recommend, and the path that doesn't strand you for two weeks while a doctor's letter is processed.</div>

<h3>Healthcare Power of Attorney (a.k.a. Healthcare Proxy)</h3>
<p>
    Authorizes someone to make <strong>medical decisions</strong>
    on the principal's behalf when they cannot. Different document
    from the financial POA, different agent allowed (often is — the
    son who lives next door makes medical calls; the CPA daughter
    handles finances).
</p>
<div class="check-item"><span class="checkbox"></span> Allows the agent to consent to or refuse treatment, choose providers, see records, authorize transfers between facilities.</div>
<div class="check-item"><span class="checkbox"></span> Activates only when the principal is determined unable to make decisions themselves — capacity standard varies by state but is usually a physician's clinical judgment.</div>
<div class="check-item"><span class="checkbox"></span> The HIPAA-release language is important — without it, providers can refuse to discuss the principal's case even with the named healthcare agent.</div>

<h3>Living Will (a.k.a. Advance Directive)</h3>
<p>
    The principal's <strong>own written instructions</strong> about
    end-of-life care, written while they still have capacity.
    Typically covers life-sustaining treatment, artificial nutrition
    and hydration, and palliative-care preferences.
</p>
<div class="check-item"><span class="checkbox"></span> Speaks for the principal when they cannot speak for themselves.</div>
<div class="check-item"><span class="checkbox"></span> Reduces the impossible burden of family members guessing what mom would have wanted.</div>
<div class="check-item"><span class="checkbox"></span> Often combined into a single "Advance Directive" form along with the healthcare proxy.</div>

<h3>POLST / MOLST / MOST (Physician/Medical Orders for Life-Sustaining Treatment)</h3>
<p>
    Distinct from the living will — these are <strong>actual medical
    orders</strong>, signed by a physician, that travel with the
    patient on a bright-pink or bright-yellow form. EMTs and ER staff
    are trained to honor them.
</p>
<div class="check-item"><span class="checkbox"></span> Appropriate for people with serious, advanced illness (advanced dementia, end-stage heart failure, late-stage cancer) — not for a healthy 60-year-old.</div>
<div class="check-item"><span class="checkbox"></span> Covers specific scenarios: CPR yes/no, intubation, hospital transfer, artificial nutrition.</div>
<div class="check-item"><span class="checkbox"></span> Without one, EMTs default to full resuscitation, every time.</div>
<div class="check-item"><span class="checkbox"></span> Naming varies by state — POLST (California, Oregon), MOLST (New York, Massachusetts), POST (Tennessee), MOST (North Carolina), and others.</div>

<h2>2. Choosing the right agent (and a backup)</h2>
<p>
    The agent's job is one of the most consequential roles a family
    member ever takes on. Pick well.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Lives near the principal</strong> if possible — proximity matters for the medical role especially.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Comfortable with conflict</strong> — agents have to make calls that other family members will sometimes disagree with, sometimes loudly.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Financially organized and trustworthy</strong> — the financial agent will see every dollar.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Willing to honor the principal's wishes, not their own</strong> — especially on end-of-life issues. An agent who can't follow "no heroic measures" should not be the agent.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Backup agent named</strong> — if the primary is sick, traveling, or has predeceased, the document still works.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Co-agents only if you must</strong> — requiring two signatures on every financial decision sounds safe but is operationally crippling.</div>

<h2>3. Executing the documents correctly</h2>
<p>
    A POA signed wrong is a POA that doesn't work when you present it
    at the bank, the facility, or the hospital. Execution matters as
    much as content.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Notarized</strong> — required in most states for financial POAs.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Witnessed</strong> — most states require 1-2 witnesses, often with rules (witnesses can't be the agent, can't be a beneficiary, can't be facility staff).</div>
<div class="check-item"><span class="checkbox"></span> <strong>State-specific form</strong> — some states accept generic forms, some have a "statutory form" that's strongly preferred or required. Healthcare proxies are especially state-specific.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Multiple originals</strong> — get at least 3 notarized originals. Banks often keep the original they're shown.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Distribute copies</strong> — the agent, the principal's primary care doctor, the local hospital (if known), and any facility the principal lives in. Don't keep all copies in a safe-deposit box (no one will be able to access them).</div>
<div class="check-item"><span class="checkbox"></span> <strong>Register the healthcare proxy / advance directive</strong> with the state registry where one exists — about half of states maintain online registries that EMS and ER staff can search.</div>

<h2>4. The state-portability question</h2>
<p>
    POAs and healthcare proxies signed in one state are <em>usually</em>
    honored in others, but enforcement is messy. If the principal
    moves or spends meaningful time in another state, re-execute the
    documents in the new state with that state's forms. Cheap
    insurance against a facility or hospital refusing to honor an
    out-of-state document at a critical moment.
</p>

<h2>5. What if no documents exist and capacity is already gone?</h2>
<p>
    The fallback path is <strong>guardianship</strong> (also called
    conservatorship in some states for the financial side). It's a
    court process — expensive, slow, often adversarial, and stripping
    of the person's autonomy.
</p>
<div class="check-item"><span class="checkbox"></span> Typical timeline: 3-9 months</div>
<div class="check-item"><span class="checkbox"></span> Typical cost: $3,000-$15,000 in attorney + court fees</div>
<div class="check-item"><span class="checkbox"></span> Requires medical evaluation, court hearing, sometimes a court-appointed evaluator independent of the family</div>
<div class="check-item"><span class="checkbox"></span> Family members can contest — the proceeding can become a fight</div>
<div class="check-item"><span class="checkbox"></span> The guardian must file annual accountings with the court — ongoing administrative burden</div>

<div class="callout" style="margin-top: 8pt;">
    <div class="label">Why this guide exists</div>
    The single most predictable family-caregiving crisis is the one
    where mom or dad declined faster than expected, no one ever filled
    out the paperwork, and now an adult child is trying to manage a
    parent's affairs without legal authority. The 90 minutes it takes
    to execute these four documents — done while capacity is still
    intact — prevents the worst version of every story that follows.
</div>

<h2>6. The action checklist</h2>
<div class="check-item"><span class="checkbox"></span> Schedule a consultation with an elder-law attorney in the principal's state (1 hour, ~$300)</div>
<div class="check-item"><span class="checkbox"></span> Have the principal execute: durable financial POA, healthcare POA, living will, HIPAA release</div>
<div class="check-item"><span class="checkbox"></span> If the principal has advanced illness, ask their physician about a POLST / MOLST</div>
<div class="check-item"><span class="checkbox"></span> Distribute originals to agent(s), primary care doctor, and any facility</div>
<div class="check-item"><span class="checkbox"></span> Register the advance directive with the state registry, if available</div>
<div class="check-item"><span class="checkbox"></span> Note the location of originals in a place the agent can find them in 5 minutes (not a safe-deposit box)</div>
<div class="check-item"><span class="checkbox"></span> Re-execute if the principal moves to another state</div>
<div class="check-item"><span class="checkbox"></span> Revisit every 3-5 years or after major life changes (death of agent, marriage, move)</div>

