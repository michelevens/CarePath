<p class="lead">
    The hospital social worker has just told you your loved one is
    being discharged in 48 hours and needs skilled-nursing care.
    You've never done this before. You're terrified you'll pick the
    wrong place — or any place at all. This guide is the next 48
    hours, hour by hour, written for the family who's already in it.
</p>

<div class="callout">
    <div class="label">What "discharge" actually means in this context</div>
    A hospital discharge to a skilled nursing facility (SNF) for
    short-term rehab is a Medicare benefit your loved one already
    qualifies for if they were a hospital inpatient for at least 3
    nights. They are not being "kicked out" — they are being moved
    to the right level of care. Medicare pays for the first 20 days
    in full, with co-pays after that. This is the easy part. The
    hard part is picking which facility.
</div>

<h2>Hour 0–4: Get the right list</h2>
<p>
    The hospital case manager will hand you a list. It is almost
    always too long, ordered alphabetically (not by quality), and
    missing the information you need to choose. Two minutes of
    cross-referencing changes everything.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Take the hospital's list.</strong> Get it in writing — name, address, phone, accepted insurance.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Cross-reference every name on CMS Nursing Home Compare</strong> (medicare.gov/care-compare). Look at the Overall Star Rating, Health Inspection Rating, and Staffing Rating. Eliminate every 1-star and 2-star facility unless they have a specific accommodation no one else has.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Look at most recent inspection.</strong> Was it in the last 12 months? Any "actual harm" or "immediate jeopardy" F-tags? Walk away.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Filter for distance.</strong> 30 minutes from family is a hard cap. You will visit daily for the first week and twice a week after that. Plan for that.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Confirm bed availability + insurance acceptance</strong> by phone with each finalist. Both can be wrong on the hospital's list.</div>

<h2>Hour 4–24: Tour the top 3</h2>
<p>
    You will not have time to tour ten places. Pick three. Visit all
    three the same day if humanly possible. Side-by-side comparison
    is what makes the decision obvious.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Call ahead — but don't let them schedule you for tomorrow.</strong> "I'm picking a discharge facility for tomorrow morning. Can I come now?" Real facilities will say yes.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Ask for the rehab unit specifically</strong> — not the long-term-care wing. Short-term rehab residents have a different environment, different staff ratios, different vibe.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Watch the call lights.</strong> Walk a hallway slowly. Count how many lights are on and how long they stay on. Anything over 4 minutes is a red flag.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Smell the air at 11am and at 3pm</strong> — the two times the staff is busiest. Persistent urine smell means understaffing.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Meet the therapy team.</strong> PT, OT, speech — they're the staff who'll actually move your loved one from "in bed" to "going home." Ask: "How many minutes of therapy per day will my mom get?" Medicare standard is 1-2 hours. Less = under-resourced.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Ask the hardest question:</strong> "What's your 30-day hospital readmission rate?" A good rehab facility knows their number and tells you. A bad one looks at their feet.</div>

<h2>Hour 24–36: Pick + advocate</h2>
<p>
    Once you've toured, decide fast. The hospital will keep pressuring
    you. Don't pick the worst option just because the social worker
    is busy.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Notify the hospital case manager</strong> of your choice. They handle the medical transfer paperwork (the "Patient Choice Form").</div>
<div class="check-item"><span class="checkbox"></span> <strong>Confirm bed-hold guarantee in writing.</strong> "If I sign today, you hold the bed until tomorrow at noon?" Get it on email.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Have your facility's admissions team coordinate directly with the hospital discharge planner</strong> — not through you. Hand them the email thread.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Get a copy of the discharge plan and orders</strong> before transport. Especially: medication list, follow-up appointments, weight-bearing status, dietary orders.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Confirm transport.</strong> Most rehab placements need either Medicare-covered ambulance (if medically necessary) or non-emergency medical transport (NEMT). Don't let them roll out in a wheelchair van for a fragile patient.</div>

<h2>Hour 36–48: The arrival itself</h2>
<p>
    You should be at the facility when your loved one arrives. The
    first 60 minutes set the tone. Be there.
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Bring:</strong> ID, insurance cards, medication list, list of current providers (PCP, specialists), durable medical equipment (CPAP, hearing aids, glasses, dentures), three sets of comfortable clothes, non-slip shoes, a few personal items (photos, blanket).</div>
<div class="check-item"><span class="checkbox"></span> <strong>Walk through the room with the admit nurse.</strong> Check: call light works, bed alarm if needed, bathroom is accessible, water pitcher within reach.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Get every name + role.</strong> Charge nurse, day-shift CNA, therapist, social worker. Write it down. Photo their badges if you need to.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Confirm the medication reconciliation.</strong> Hospital → SNF medication errors are the #1 cause of preventable readmission. Sit with the nurse and verify every med, dose, time.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Ask: "Who do I call after hours?"</strong> Get the direct extension, not the main line.</div>

<h2>The day-after audit</h2>
<p>
    Return within 24 hours of admit. Check on:
</p>
<div class="check-item"><span class="checkbox"></span> Has therapy started? (Should be within 24-48 hours)</div>
<div class="check-item"><span class="checkbox"></span> Are vitals being taken? Pain managed?</div>
<div class="check-item"><span class="checkbox"></span> Are they eating? Are meals being assisted if needed?</div>
<div class="check-item"><span class="checkbox"></span> Is the room clean? Linens fresh?</div>
<div class="check-item"><span class="checkbox"></span> Have you been called by the SNF if anything changed?</div>

<div class="callout" style="margin-top: 8pt;">
    <div class="label">If something is wrong, escalate fast</div>
    Day 1 issues = the next 20 days will be worse. The escalation
    path: charge nurse → director of nursing → administrator → the
    state ombudsman (every state has one, every facility must post
    the phone number). If you suspect serious neglect or abuse, call
    the state long-term care ombudsman directly — it's free and
    confidential.
</div>

<h2>The 100-day Medicare clock</h2>
<p>
    Medicare's SNF benefit covers the first 100 days per "benefit
    period." Days 1-20 are fully covered. Days 21-100 have a daily
    co-pay (~$200/day in 2026, indexed annually). After day 100, the
    benefit ends until a new benefit period begins (60 consecutive
    days out of any covered care).
</p>
<div class="check-item"><span class="checkbox"></span> <strong>Track the days.</strong> The SNF will not always remind you when day 20 ends.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Discuss discharge from day 7.</strong> Where is your loved one going next? Home with home health? Assisted living? Long-term Medicaid SNF? Plan for it now.</div>
<div class="check-item"><span class="checkbox"></span> <strong>Watch for premature discharge.</strong> If the facility says they can't bill Medicare anymore but your loved one isn't ready, you have appeal rights. They must give you a written notice — the "Notice of Medicare Non-Coverage." File the appeal with the Quality Improvement Organization (QIO) for your state. Free, fast, often successful.</div>

