<p class="lead">
    The hardest conversation in the family doesn't start with a fall
    or a hospital stay. It starts with a series of small signs you
    talked yourself out of for two years. This guide is the
    framework for noticing them honestly — nine concrete trigger
    signs, what each one means, and what to do in the week after you
    finally admit what you've been seeing.
</p>

<div class="callout">
    <div class="label">Who this guide is for</div>
    Adult children, spouses, and other caregivers of someone who is
    "doing fine, mostly" — but isn't. Written for the moment you've
    already had the thought "maybe Mom shouldn't be alone" and need
    a framework for what to do next.
</div>

<h2>The 9 trigger signs (in order of severity)</h2>

<h3>1. Unexplained weight loss</h3>
<p>
    The body's quietest cry for help. Look for: clothes hanging
    loose, belt notches added, dramatic body-shape changes since the
    last family visit. Common causes: depression, undiagnosed
    illness, difficulty cooking, swallowing problems, forgetting to
    eat, financial trouble. Any of these warrants a doctor's visit
    and a hard look at meal-prep capacity.
</p>

<h3>2. Medication mistakes</h3>
<p>
    Pill bottles found in the wrong rooms, full bottles when they
    should be half-empty, the same bottle empty months early. A
    weekly pill organizer they used to manage flawlessly is suddenly
    skipping days or doubling doses. Medication errors are the #1
    preventable cause of ER visits for elders living alone.
</p>

<h3>3. Hygiene decline</h3>
<p>
    Wearing the same outfit two days in a row. Hair not brushed.
    Body odor where there never was one. Toilet paper running low.
    The bathroom smelling like urine because they're missing the
    toilet at night. Often the most painful sign to acknowledge
    because of what it implies about dignity — but it's
    overwhelmingly the most reliable early signal.
</p>

<h3>4. Falls (any number, even "small")</h3>
<p>
    The first fall doubles the risk of a second. The first
    fracture-causing fall has a roughly 25% one-year mortality rate
    for adults over 75. Any fall — even one that didn't cause an
    injury — is a leading indicator that the home environment is no
    longer safe for unsupervised living.
</p>

<h3>5. Repeated stories + lost names</h3>
<p>
    Telling the same story 20 minutes apart in the same
    conversation. Pausing mid-sentence to find a familiar word.
    Calling grandchildren by the wrong name (more than once). These
    are pre-dementia warning signs, not "normal aging." A baseline
    cognitive screen from their PCP is the next step.
</p>

<h3>6. Bills going unpaid</h3>
<p>
    Stacks of unopened mail. Past-due notices. Utilities being shut
    off. A previously diligent person who's now letting things
    slide. Sometimes the first sign of cognitive decline, sometimes
    pure overwhelm. Either way, financial harm follows fast —
    elders living alone are the #1 target for scams precisely
    because of this signal.
</p>

<h3>7. Refrigerator full of expired or rotting food</h3>
<p>
    The check that takes 30 seconds. Open the fridge. If half the
    contents are past their dates, if there are leftovers from weeks
    ago, if the produce drawer is liquefying — they're not eating,
    they're not seeing what's in there, and they're at risk of
    foodborne illness on top of the under-nutrition.
</p>

<h3>8. Driving incidents (or driving avoidance)</h3>
<p>
    New dents and scrapes on the car they can't explain. Getting
    lost on routes they've driven for 40 years. Other people calling
    you ("I saw your mom in the parking lot, she seemed confused").
    OR — equally significant — quietly refusing to drive anymore
    because they don't trust themselves. That's wisdom; it's also a
    massive loss of independence that needs a plan.
</p>

<h3>9. Social isolation</h3>
<p>
    Stopped attending church / club / standing weekly lunch. Doesn't
    answer the phone. Doesn't initiate. Doesn't have anyone
    checking on them between your visits. Isolation is both a sign
    of underlying decline and a multiplier of every other problem
    — a fall when no one knows you fell is a different magnitude of
    crisis than a fall in a community setting.
</p>

<h2>How many is too many?</h2>
<div class="callout">
    <div class="label">The rule of thumb most geriatricians use</div>
    <strong>One sign is a flag to watch.</strong> Talk to their PCP,
    increase the frequency of check-ins, install a medical-alert
    button.
    <br><br>
    <strong>Two or three is a serious conversation.</strong> Time to
    explore in-home help (paid caregiver, family rotation), home
    modifications, an Adult Day program, or a senior-community move
    while it's still a choice rather than a crisis.
    <br><br>
    <strong>Four or more is a decision week.</strong> The window for
    a planned, supported move to assisted living, memory care, or a
    family member's home is closing. Don't wait for the fall that
    forces the move at 3am from an ER.
</div>

<h2>The 4 things to do the week you notice</h2>

<h3>1. Document what you're seeing</h3>
<p>
    Keep a 2-week log: date, what happened, severity. This isn't
    surveillance, it's the conversation prep. When you later sit
    down with siblings or with your loved one, you have a list
    instead of an impression. "I'm worried" is dismissable; "Here
    are nine things in fourteen days" is not.
</p>

<h3>2. Get an objective assessment</h3>
<p>
    Their PCP can do a 15-minute cognitive screen (MoCA or MMSE)
    and a basic ADL assessment. Medicare covers an annual wellness
    visit that includes a cognitive screening. Ask for it.
    Alternatively, hire a Geriatric Care Manager (now often called
    Aging Life Care Professionals) for an in-home assessment — $300-
    600, single best money in this category. They produce a written
    report you can act on and share with siblings.
</p>

<h3>3. Have the conversation early</h3>
<p>
    The conversation goes better when your loved one still has the
    cognitive bandwidth to be part of the decision. The script that
    works: "I've noticed [specific things]. I'm not trying to take
    away your independence. I want to be ahead of the next thing
    that could go wrong, with you, not for you. What would you want
    if [scenario]?"
</p>

<h3>4. Get the legal paperwork done immediately</h3>
<p>
    Power of attorney (financial + healthcare), HIPAA release, and
    a living will / advance directive. While capacity is intact and
    the conversation is calm. Once capacity slips, guardianship
    becomes the only path — slow, expensive, adversarial. See our
    POA + healthcare proxy walkthrough guide.
</p>

<h2>The conversation no one wants to have</h2>
<p>
    Eighty-percent of placements happen <strong>after</strong> a
    crisis (ER visit, fall, sudden decline) — when the family has
    no time to compare, tour, or plan. The remaining twenty percent
    happen <strong>before</strong> the crisis. Those families end
    up at a facility their loved one chose with them, in a unit
    they preferred, at a price they actually budgeted for.
</p>

<div class="callout" style="margin-top: 14pt; background:#F4F4F2;">
    <div class="label">Bottom line</div>
    The signs are almost always there a year before the family is
    ready to admit them. The cost of acting six months early is some
    awkward conversations and the discomfort of disrupting a status
    quo. The cost of acting six months late is the worst week of
    your family's life. Read the signs honestly.
</div>

