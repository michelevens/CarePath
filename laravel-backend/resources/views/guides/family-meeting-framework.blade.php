@section('content')

<p class="lead">
    Three adult children, three opinions, one mother who needs
    care now. The conversation no one wants to host is the one
    that determines everything that happens next. This guide is
    the framework — how to call the meeting, what to put on the
    agenda, how to run it when emotions run hot, and what to
    decide vs what to defer.
</p>

<div class="callout">
    <div class="label">Who calls the meeting</div>
    Whoever is closest to the situation — geographically or
    practically — usually has to. Waiting for "everyone to agree
    we should meet" is how families stay stuck. Send the invite
    even if your siblings push back. The first meeting is rarely
    the productive one; the second one almost always is.
</div>

<h2>1. Decide who's at the table</h2>

<p><strong>Always:</strong></p>
<div class="check-item"><span class="checkbox"></span> All adult children (or stepchildren actively involved in care)</div>
<div class="check-item"><span class="checkbox"></span> The spouse / partner of the person needing care, if living</div>

<p><strong>Sometimes:</strong></p>
<div class="check-item"><span class="checkbox"></span> The person needing care themselves — if cognition + emotional capacity allow; their voice is the most important. Skip only when active dementia would derail the conversation.</div>
<div class="check-item"><span class="checkbox"></span> Spouses of adult children — for support; not as decision-makers</div>
<div class="check-item"><span class="checkbox"></span> A neutral third party (Geriatric Care Manager, elder-law attorney, family therapist) — strongly recommended when there's existing conflict</div>

<p><strong>Rarely:</strong></p>
<div class="check-item"><span class="checkbox"></span> Adult grandchildren (unless they're primary caregivers)</div>
<div class="check-item"><span class="checkbox"></span> Family friends, neighbors, clergy (unless specifically invited by the person needing care)</div>

<h2>2. Send the invite right</h2>

<p>The most effective family-meeting invitation has 4 elements:</p>

<div class="callout" style="margin-top: 8pt;">
    <div class="label">Sample text</div>
    "I want us to all sit down together to make a plan for Mom's
    care over the next 6-12 months. I'm not asking for a decision
    in the meeting itself — just for us to be in the same room
    looking at the same information together. <br><br>
    Proposed: Sunday March 8 at 4pm, video call (Zoom link
    below). Will run about 90 minutes. <br><br>
    Before the meeting please look at the attached: Mom's most
    recent doctor's note, the 9 trigger signs guide, and her
    current monthly expenses. <br><br>
    Please reply by Wednesday whether you can join. If you can't
    make it, I'll send a summary afterward."
</div>

<p><strong>What that does</strong> — sets a clear ask, sets a clear
end-state (no decision needed yet), pre-reads the prep material,
gives people an out, and signals you'll proceed without them if
they can't show up. All four matter.</p>

<h2>3. The 5-section agenda</h2>

<h3>1. What's the actual situation? (15 min)</h3>
<p>
    One person — typically the closest caregiver — presents the
    facts. Specific. Recent. Documented. "Here are the 14 things
    I've observed in the last 30 days." Not opinion. Not "I'm
    worried." Specifics.
</p>

<h3>2. What does Mom want? (10 min)</h3>
<p>
    If Mom is in the meeting, she speaks here. If she's not — the
    person closest to her summarizes her expressed wishes from
    recent conversations. Critical: don't substitute the family's
    wishes for hers. "She wants to stay at home as long as
    possible" is a wish; "she'll be safer at home" is the
    family's interpretation. Keep them distinct.
</p>

<h3>3. What can each of us actually contribute? (20 min)</h3>
<p>
    Go around. Each person says what they're realistically able
    to provide:
</p>
<div class="check-item"><span class="checkbox"></span> Time (hours/week, which hours, what tasks)</div>
<div class="check-item"><span class="checkbox"></span> Money (monthly contribution, lump sum, willingness to advance against inheritance)</div>
<div class="check-item"><span class="checkbox"></span> Coordination (managing appointments, paperwork, communications)</div>
<div class="check-item"><span class="checkbox"></span> Backup / respite (covering when primary caregiver needs a break)</div>

<p>
    Honesty here matters more than fairness. "I can pay $400/mo"
    is workable; "I'll help whenever I can" is not. Specifics.
</p>

<h3>4. What are our options? (25 min)</h3>
<p>
    Three to five concrete scenarios, costed roughly. Not
    finalized — but enough to compare:
</p>
<div class="check-item"><span class="checkbox"></span> Stay at home with X hours/week of paid care + Y from family</div>
<div class="check-item"><span class="checkbox"></span> Move to assisted living near [child A]</div>
<div class="check-item"><span class="checkbox"></span> Move in with [child B / spouse]</div>
<div class="check-item"><span class="checkbox"></span> Stay at home, add adult day program 3 days/week</div>
<div class="check-item"><span class="checkbox"></span> Continuing care community (long-game)</div>

<h3>5. What do we decide today vs defer? (20 min)</h3>
<p>
    Most meetings will not produce the placement decision. They
    will produce smaller decisions that unblock it. Common
    "decide today" items:
</p>
<div class="check-item"><span class="checkbox"></span> Who is point-of-contact for the doctor / care team going forward?</div>
<div class="check-item"><span class="checkbox"></span> Who handles bills + finances?</div>
<div class="check-item"><span class="checkbox"></span> When do we get legal paperwork (POA, healthcare proxy) in place?</div>
<div class="check-item"><span class="checkbox"></span> Who tours which facilities, by when?</div>
<div class="check-item"><span class="checkbox"></span> When do we meet again?</div>

<h2>4. The 4 conflicts that derail meetings (and how to handle each)</h2>

<h3>"You weren't there for the last 10 years — don't tell us what to do now"</h3>
<p>
    Surfaces when an absent sibling (geographically distant, often
    affluent) tries to dictate without context. The closer
    caregiver feels overruled. <strong>Disarm</strong>: have the
    closer caregiver own information ("here's what I've been
    seeing"); let the further sibling own resources ("here's what I
    can contribute"). Roles, not rank.
</p>

<h3>"Mom would never want to leave her home"</h3>
<p>
    Often said by the sibling least involved in daily caregiving —
    a way to vote against placement without acknowledging the
    caregiver burnout that's driving the conversation. <strong>
    Disarm</strong>: separate Mom's wishes (real, important) from
    the family's capacity to honor them (also real, also
    important). "What Mom wants" and "what the family can sustain
    delivering" are two different conversations. Both have to be
    addressed.
</p>

<h3>"We can't afford that"</h3>
<p>
    Almost always true and almost always paralyzing. <strong>
    Disarm</strong>: bring the LTC Cost Reality Check guide. Run
    the 60-second runway math together. Confirm what each funding
    source covers. The numbers usually look different on paper
    than in the family's heads.
</p>

<h3>"You're trying to take her money"</h3>
<p>
    The accusation no one wants to hear. Usually rooted in older
    family dynamics, not current behavior. <strong>Disarm</strong>:
    transparency. Have a shared spreadsheet of monthly income,
    monthly expenses, account balances. Make decisions in writing.
    Suggest a neutral fiduciary (estate attorney, professional
    trustee) if trust is fundamentally broken.
</p>

<h2>5. After the meeting</h2>

<div class="check-item"><span class="checkbox"></span> Send a written summary within 24 hours — decisions made, decisions deferred, action items + owners + due dates</div>
<div class="check-item"><span class="checkbox"></span> Schedule the follow-up meeting before this one ends — momentum dies without it</div>
<div class="check-item"><span class="checkbox"></span> Set a shared place to document everything (Google Doc, shared folder, simple email thread)</div>
<div class="check-item"><span class="checkbox"></span> Update Mom on what was decided — even if she wasn't in the meeting, she shouldn't be the last to know</div>
<div class="check-item"><span class="checkbox"></span> Get the legal paperwork started <strong>immediately</strong> if it's not already done — POA + healthcare proxy + living will. See our walkthrough guide.</div>

<h2>When you need outside help</h2>

<p>Hire a neutral third party if any of these apply:</p>
<div class="check-item"><span class="checkbox"></span> One sibling has consistently dominated past family decisions</div>
<div class="check-item"><span class="checkbox"></span> There's a history of estrangement or major conflict</div>
<div class="check-item"><span class="checkbox"></span> Significant assets / inheritance create financial conflicts of interest</div>
<div class="check-item"><span class="checkbox"></span> Mom's wishes are contested (one sibling says she'd want X, another says Y)</div>
<div class="check-item"><span class="checkbox"></span> The conversations keep ending in tears or shouting</div>

<p style="margin-top: 8pt;">
    <strong>Who to call:</strong>
</p>
<div class="check-item"><span class="checkbox"></span> Geriatric Care Manager (Aging Life Care Professional) — for the situational assessment</div>
<div class="check-item"><span class="checkbox"></span> Elder-law attorney — for the legal + financial structure</div>
<div class="check-item"><span class="checkbox"></span> Family therapist or mediator — for the conflict itself</div>
<div class="check-item"><span class="checkbox"></span> A neutral clergy member, if the family has that kind of relationship</div>

<div class="callout" style="margin-top: 18pt; background:#F4F4F2;">
    <div class="label">Bottom line</div>
    The family meeting isn't the moment everyone agrees. It's the
    moment everyone is looking at the same information at the same
    time. That alone is rarer than you'd think — and almost always
    moves the family further forward in 90 minutes than the
    previous 9 months of side conversations did.
</div>

