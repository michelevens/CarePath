<h2>1. The in-home aide</h2>

<h3>What it actually is</h3>
<p>
    A paid caregiver (CNA, home health aide, or non-medical
    companion) comes to your loved one's home for a defined number
    of hours per week. Tasks: meal prep, bathing assistance, light
    cleaning, medication reminders, companionship, transport.
</p>

<h3>What it costs</h3>
<p>
    Median $30-40 per hour through a home-care agency, $20-25 if
    you hire privately (but then you're the employer — withholding,
    insurance, vacation). A "full coverage" 8 hours/day, 5 days a
    week schedule runs $6,000-8,000/month — comparable to memory
    care monthly, but for less coverage.
</p>

<h3>When it's the right answer</h3>
<div class="check-item"><span class="checkbox"></span> Early to mid-stage dementia, still socially engaged at home</div>
<div class="check-item"><span class="checkbox"></span> The primary caregiver works full-time and needs daytime coverage</div>
<div class="check-item"><span class="checkbox"></span> Strong attachment to the home / community / neighborhood</div>
<div class="check-item"><span class="checkbox"></span> Family has the capacity to handle nights + weekends</div>
<div class="check-item"><span class="checkbox"></span> Wandering risk is low</div>

<h3>When it stops working</h3>
<div class="check-item"><span class="checkbox"></span> Wandering becomes a real risk and you can't supervise overnight</div>
<div class="check-item"><span class="checkbox"></span> Sundowning is severe and the family caregiver isn't sleeping</div>
<div class="check-item"><span class="checkbox"></span> The cost crosses $7,000/month for partial coverage — at that point memory care is cheaper for more coverage</div>

<h2>2. The adult day program</h2>

<h3>What it actually is</h3>
<p>
    A licensed facility — sometimes a standalone "Adult Day Health
    Center," sometimes part of an assisted-living or memory-care
    community — that takes in adults during weekday hours. Your
    loved one gets dropped off in the morning, picked up in late
    afternoon. They go home with you to sleep.
</p>

<p>
    Programs vary widely. The <strong>social model</strong> focuses
    on activities, conversation, and engagement — like preschool
    for adults. The <strong>medical model</strong> adds
    nursing-level oversight (medication management, vital signs,
    rehab therapy) for residents with more medical needs.
</p>

<h3>What it costs</h3>
<p>
    $80-120 per day at the standard rate. Significantly cheaper
    than an in-home aide for the same hours. Medicaid HCBS waivers
    in many states cover it; some VA benefits cover it; long-term
    care insurance often covers it. Always cheaper than memory
    care monthly.
</p>

<h3>When it's the right answer</h3>
<div class="check-item"><span class="checkbox"></span> Early to mid-stage dementia, still socially capable in group settings</div>
<div class="check-item"><span class="checkbox"></span> The primary caregiver works during the day and can manage evenings + nights</div>
<div class="check-item"><span class="checkbox"></span> Your loved one is isolated at home and would benefit from social stimulation</div>
<div class="check-item"><span class="checkbox"></span> You need predictable respite a few days a week</div>
<div class="check-item"><span class="checkbox"></span> Costs make memory care infeasible right now</div>

<h3>When it stops working</h3>
<div class="check-item"><span class="checkbox"></span> Behaviors become disruptive in group settings (aggression, severe agitation)</div>
<div class="check-item"><span class="checkbox"></span> Toileting / dressing assistance exceeds what the program can provide</div>
<div class="check-item"><span class="checkbox"></span> Transportation logistics become exhausting</div>
<div class="check-item"><span class="checkbox"></span> Evenings + nights at home become the worst part of every day</div>

<h2>3. Memory care</h2>

<h3>What it actually is</h3>
<p>
    A residential community specifically designed for people with
    dementia. Distinct from regular assisted living: secured
    perimeter (wandering prevention), specialty-trained staff,
    structured activities calibrated for cognitive impairment,
    smaller resident populations, dementia-friendly architecture
    (large signs, color cues, secured outdoor courtyards).
</p>

<h3>What it costs</h3>
<p>
    $6,000-9,000/month median nationally (significantly higher in
    coastal metros). Most state Medicaid programs don't cover
    memory care — though some HCBS waivers and a handful of
    state-supplement programs do.
</p>

<h3>When it's the right answer</h3>
<div class="check-item"><span class="checkbox"></span> Mid-to-late stage dementia with consistent supervision needs</div>
<div class="check-item"><span class="checkbox"></span> Wandering with elopement risk</div>
<div class="check-item"><span class="checkbox"></span> Caregiver burnout reaching the breaking point</div>
<div class="check-item"><span class="checkbox"></span> Family bandwidth + finances allow the transition</div>
<div class="check-item"><span class="checkbox"></span> Your loved one's safety at home is no longer assured 24/7</div>

<h3>When it doesn't (yet)</h3>
<div class="check-item"><span class="checkbox"></span> Very early-stage with strong home attachment + low risk profile</div>
<div class="check-item"><span class="checkbox"></span> Family is managing well and your loved one is thriving at home</div>
<div class="check-item"><span class="checkbox"></span> Costs would force a Medicaid spend-down before they need full coverage</div>

