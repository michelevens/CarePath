<h2>The progression most families actually go through</h2>

<p>It rarely jumps from "fine" to "memory care." The common path:</p>
<div class="check-item"><span class="checkbox"></span> <strong>Stage 1 (mild):</strong> Family + occasional respite</div>
<div class="check-item"><span class="checkbox"></span> <strong>Stage 2 (mid):</strong> Adult day program 2-3 days/week + family</div>
<div class="check-item"><span class="checkbox"></span> <strong>Stage 3 (mid-progressing):</strong> Adult day program full-time + in-home aide for nights</div>
<div class="check-item"><span class="checkbox"></span> <strong>Stage 4 (late):</strong> Memory care residential</div>
<div class="check-item"><span class="checkbox"></span> <strong>Stage 5 (advanced):</strong> Memory care + hospice on-site</div>

<div class="callout" style="margin-top: 18pt; background:#F4F4F2;">
    <div class="label">Bottom line</div>
    The biggest mistake families make is treating this as a binary
    "home vs facility" choice. The reality is a 5-step progression,
    and the in-between steps — especially adult day programs and
    in-home aides — buy years of better quality of life at lower
    cost. Don't skip them just because they're less visible than
    the memory-care option.
