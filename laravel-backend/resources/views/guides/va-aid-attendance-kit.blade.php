<h2>Step-by-step application</h2>
<ol>
    <li><strong>Confirm eligibility</strong> using the 30-second check above. If unsure, the VA offers a free benefits assistance call: 1-800-827-1000.</li>
    <li><strong>Get VA Form 21-2680 filled out by your doctor.</strong> This is the single most-important document — without a clear medical statement of need, applications get denied or downgraded to housebound only (lower benefit).</li>
    <li><strong>File VA Form 21P-527EZ</strong> (veteran) or <strong>21P-534EZ</strong> (surviving spouse). Online at va.gov, by mail to your state's Pension Management Center, or with help from an accredited VSO.</li>
    <li><strong>Attach all required documents</strong> from the checklist above.</li>
    <li><strong>Wait.</strong> Processing typically takes 3–8 months. Benefits are paid <em>retroactive to the application date</em>, so submit as early as you can.</li>
    <li><strong>Respond to VA requests promptly.</strong> Most denials are paperwork-fixable; respond within the deadline given.</li>
</ol>

<h2>Where to get free, accredited help</h2>
<ul>
    <li><strong>State Veterans Affairs offices</strong> — every state has one; many counties too. Find yours at va.gov/find-locations.</li>
    <li><strong>Accredited Veteran Service Organizations (VSOs)</strong>: VFW, American Legion, DAV, AMVETS. All offer free claim assistance — search "VSO near me" at va.gov.</li>
    <li><strong>National Association of Veterans' Advocates</strong>: navadvocates.org — accredited attorneys (paid).</li>
</ul>

<div class="callout">
    <div class="label">Two critical warnings</div>
    <strong>1.</strong> Never pay an unaccredited "VA pension planner" who offers to file your claim — it's illegal under federal law and they often steer applicants into bad annuities. Only VA-accredited attorneys, claims agents, or VSOs may charge or assist with VA claims.
    <br><br>
    <strong>2.</strong> If you receive Aid &amp; Attendance, you generally CAN still receive Medicaid for nursing-home care later — but the benefit converts to a lower nursing-home allowance ($90/mo) once Medicaid pays for care. Coordinate planning with an elder-law attorney before applying for both.
</div>

<h2>Common reasons claims get denied (and how to avoid them)</h2>
<table>
    <thead><tr><th>Reason</th><th>Fix</th></tr></thead>
    <tbody>
        <tr><td>Missing or incomplete VA Form 21-2680</td><td>Have doctor be specific: list the exact ADLs the applicant needs help with, plus diagnoses</td></tr>
        <tr><td>Net worth over the limit</td><td>Subtract monthly unreimbursed medical expenses × 12 from countable assets — many applicants drop below the limit this way</td></tr>
        <tr><td>Wrong service dates (not wartime)</td><td>Check the exact dates listed above; even one qualifying day is enough</td></tr>
        <tr><td>DD-214 missing</td><td>Request a free copy at vetrecs.archives.gov — usually arrives in 30–90 days</td></tr>
        <tr><td>Income statement omits medical costs</td><td>List every UME (unreimbursed medical expense) including premiums, copays, facility cost, in-home aides</td></tr>
    </tbody>
</table>

