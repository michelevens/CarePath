<p class="lead">
    Aid &amp; Attendance is a monthly cash benefit on top of the VA pension — up to $2,727/mo for a single veteran or $3,232/mo for a veteran and spouse, tax-free. It pays for assisted living, in-home care, or skilled nursing. Most eligible families never apply.
</p>

<h2>The 30-second eligibility check</h2>
<p>You're likely eligible if all of these are true:</p>
<ul>
    <li>The veteran served <strong>90+ days of active duty</strong> with at least 1 day during a wartime period</li>
    <li>Discharge was <strong>not dishonorable</strong></li>
    <li>The applicant (veteran or surviving spouse) needs help with <strong>at least 2 activities of daily living</strong> — bathing, dressing, eating, toileting, transferring, walking — <em>or</em> is bedridden, in a nursing home, or legally blind</li>
    <li>Household <strong>net worth</strong> (assets + income, minus debts and unreimbursed medical expenses) is under <strong>$159,240</strong> (2026 limit, adjusts annually)</li>
</ul>

<h2>2026 maximum monthly benefits</h2>
<table>
    <thead>
        <tr><th>Recipient</th><th>Maximum monthly benefit</th></tr>
    </thead>
    <tbody>
        <tr><td>Single veteran</td><td>$2,727</td></tr>
        <tr><td>Veteran with one dependent (spouse)</td><td>$3,232</td></tr>
        <tr><td>Two veterans married to each other</td><td>$4,324</td></tr>
        <tr><td>Surviving spouse of a veteran</td><td>$1,754</td></tr>
    </tbody>
</table>
<p class="small">Actual benefit is the maximum minus your countable income. Unreimbursed medical expenses (including LTC facility cost) reduce countable income — which is why many applicants who think they "earn too much" actually qualify.</p>

<h2>Qualifying wartime periods</h2>
<ul>
    <li>World War II: Dec 7, 1941 – Dec 31, 1946</li>
    <li>Korean conflict: Jun 27, 1950 – Jan 31, 1955</li>
    <li>Vietnam era: Feb 28, 1961 – May 7, 1975 (Aug 5, 1964 – May 7, 1975 for non-in-country)</li>
    <li>Gulf War: Aug 2, 1990 – present (officially still open)</li>
</ul>
<p>Service does not need to have been in combat or in the war zone — just during the period.</p>

<h2>The 3-year look-back (for transfers)</h2>
<p>Like Medicaid, the VA reviews asset transfers — but for <strong>36 months (3 years)</strong>, not 5. Transfers that put your net worth below the limit may trigger a penalty period (up to 5 years). Common penalty-trigger transfers:</p>
<ul>
    <li>Cash gifts to family</li>
    <li>Adding others to deeds</li>
    <li>Transfers to most trusts</li>
    <li>Selling assets below market value</li>
</ul>
<p>Spend-down on goods/services received at fair value is allowed (home repairs, medical equipment, debt payoff, prepaid funeral, attorney fees).</p>

