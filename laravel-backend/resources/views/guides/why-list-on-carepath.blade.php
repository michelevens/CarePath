<h2>5. The CMS Five-Star reality you can't hide from</h2>
<p>
    Your CMS Nursing Home Compare rating, your last inspection date,
    and your F-tag deficiencies are all <strong>public federal records</strong>
    (or state equivalents). They appear on your CarePath page whether
    you claim or not — there's no setting to hide them, and there shouldn't
    be.
</p>
<p>
    What changes when you claim: you can add context. A 3-star rating
    next to a manager-written response — "We hired a new DON in March
    2026 after the prior inspection and rolled out a fall-prevention
    program. Our next inspection is scheduled for Q3" — is read by
    families as ownership and accountability. The same 3 stars with no
    context reads as something to avoid.
</p>

